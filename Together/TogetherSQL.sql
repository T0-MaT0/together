-- 2. 테이블 생성 (FK 순서 고려)
-- 회원 정보 테이블
CREATE TABLE MEMBER (
    MEMBER_NO NUMBER PRIMARY KEY, -- 회원 고유 번호
    MEMBER_ID VARCHAR2(30) NOT NULL UNIQUE, -- 회원 아이디
    MEMBER_PW VARCHAR2(100) NOT NULL, -- 회원 비밀번호 (암호화 저장)
    MEMBER_NICK VARCHAR2(30) NOT NULL, -- 닉네임
    MEMBER_NAME VARCHAR2(30) NOT NULL, -- 회원 이름
    MEMBER_ADDR VARCHAR2(500), -- 회원 주소
    MEMBER_TEL CHAR(11) NOT NULL, -- 회원 전화번호
    PROFILE_IMG VARCHAR2(300), -- 프로필 이미지 URL
    ENROLL_DATE DATE DEFAULT SYSDATE, -- 가입일
    MEMBER_EMAIL VARCHAR2(50) NOT NULL UNIQUE, -- 이메일
    MEMBER_DEL_FL CHAR(1) DEFAULT 'N' CHECK (MEMBER_DEL_FL IN ('Y', 'N', 'B')), -- 탈퇴여부(N:탈퇴X, Y: 탈퇴O B:블랙)
    AUTHORITY NUMBER DEFAULT 2, -- 권한 (관리자=1, 일반회원=2, 브랜드=3)
    MEMBER_GRADE NUMBER DEFAULT 9, -- 회원 등급
    MEMBER_BIRTH DATE NOT NULL, -- 생년월일
    POINT NUMBER DEFAULT 0 -- 보유 포인트
);

-- 사업자 정보 테이블
CREATE TABLE COMPANY (
    MEMBER_NO NUMBER PRIMARY KEY, -- 회원 고유 번호 (사업자)
    BUSINESS_NO NUMBER NOT NULL UNIQUE, -- 사업자 등록 번호
    BANK_NAME VARCHAR2(1500) NOT NULL, -- 은행명
    BANK_NO VARCHAR2(3000) NOT NULL, -- 계좌번호
    PERMISSION_FL CHAR(1) DEFAULT 'N' CHECK (PERMISSION_FL IN ('Y', 'N')), -- 상품 판매 허가 여부
    FOREIGN KEY (MEMBER_NO) REFERENCES MEMBER(MEMBER_NO)
);

-- 게시판 종류 테이블
CREATE TABLE BOARD_TYPE (
    BOARD_CD NUMBER PRIMARY KEY, -- 게시판 코드(1:모집, 2:브랜드, 3:공지사항, 4: FAQ, 5 :문의,6: 1대1문의 , 7: 광고 문의 ,8:제휴문의, 9:회원/계정, 10:공동구매, 11: 결제/환불, 12:수령/배송 13:기타)
    BOARD_NAME VARCHAR2(50) NOT NULL, -- 게시판 이름
    PARENT_BOARD_CD NUMBER, -- 부모 게시판 코드(1:모집, 2:브랜드, 3:공지사항, 4: FAQ 5:문의)
    FOREIGN KEY (PARENT_BOARD_CD) REFERENCES BOARD_TYPE(BOARD_CD) 
);

-- 게시판 정보 테이블
CREATE TABLE BOARD (
    BOARD_NO NUMBER PRIMARY KEY, -- 게시글 고유 번호
    BOARD_TITLE VARCHAR2(100) NOT NULL, -- 게시글 제목
    BOARD_CONTENT VARCHAR2(2000) NOT NULL, -- 게시글 내용
    B_CREATE_DATE DATE DEFAULT SYSDATE NOT NULL, -- 작성일
    B_UPDATE_DATE DATE, -- 수정일
    BOARD_DEL_FL CHAR(1) DEFAULT 'N' CHECK (BOARD_DEL_FL IN ('Y', 'N')), -- 게시글 삭제 여부
    B_STATE VARCHAR2(50), -- 문의 상태
    MEMBER_NO NUMBER NOT NULL, -- 작성자 회원 번호
    BOARD_CD NUMBER NOT NULL, -- 게시판 코드
    FOREIGN KEY (MEMBER_NO) REFERENCES MEMBER(MEMBER_NO),
    FOREIGN KEY (BOARD_CD) REFERENCES BOARD_TYPE(BOARD_CD)
);

-- 상품 카테고리 테이블
CREATE TABLE CATEGORY (
    CATEGORY_NO NUMBER PRIMARY KEY, -- 카테고리 번호
    CATEGORY_NAME VARCHAR2(50) NOT NULL, -- 카테고리 이름
    PARENT_CATEGORY_NO NUMBER, -- 부모 카테고리 번호
    FOREIGN KEY (PARENT_CATEGORY_NO) REFERENCES CATEGORY(CATEGORY_NO)
);

-- 상품 정보 테이블
CREATE TABLE PRODUCT (
    BOARD_NO NUMBER PRIMARY KEY, -- 게시글 번호 (상품 게시글)
    PRODUCT_PRICE NUMBER NOT NULL, -- 상품 가격
    PRODUCT_COUNT NUMBER NOT NULL, -- 상품 재고 수량
    DELIVERY_FEE NUMBER NOT NULL, -- 배송비
    READ_COUNT NUMBER DEFAULT 0, -- 조회수
    CATEGORY_NO NUMBER NOT NULL, -- 카테고리 번호
    FOREIGN KEY (CATEGORY_NO) REFERENCES BOARD(BOARD_NO)
);


-- 상품 옵션 테이블
CREATE TABLE PRODUCT_OPTION (
    OPTION_NO NUMBER PRIMARY KEY, -- 옵션 번호
    OPTION_NAME VARCHAR2(50) NOT NULL, -- 옵션 이름
    BOARD_NO NUMBER NOT NULL, -- 게시글 번호
    FOREIGN KEY (BOARD_NO) REFERENCES BOARD(BOARD_NO)
);

-- 주문 테이블
CREATE TABLE "ORDER" (
    ORDER_NO NUMBER PRIMARY KEY, -- 주문 고유 번호
    QUANTITY NUMBER NOT NULL, -- 구매 수량
    DELIVERY_ADDR VARCHAR2(500) NOT NULL, -- 배송지
    TRACKING_NO VARCHAR2(50), -- 운송장 번호 (배송 시작 후 부여)
    BOARD_NO NUMBER NOT NULL, -- 게시글 번호
    MEMBER_NO NUMBER NOT NULL, -- 주문한 회원 번호
    OPTION_NO NUMBER NOT NULL, -- 주문한 옵션 번호
    FOREIGN KEY (BOARD_NO) REFERENCES BOARD(BOARD_NO),
    FOREIGN KEY (MEMBER_NO) REFERENCES MEMBER(MEMBER_NO),
    FOREIGN KEY (OPTION_NO) REFERENCES PRODUCT_OPTION(OPTION_NO)
);

-- 리뷰 테이블
CREATE TABLE REVIEW (
    REVIEW_NO NUMBER PRIMARY KEY, -- 리뷰 번호
    REVIEW_CONTENT VARCHAR2(3000) NOT NULL, -- 리뷰 내용
    REVIEW_STAR NUMBER NOT NULL, -- 별점
    REVIEW_CREATED_DATE DATE DEFAULT SYSDATE NOT NULL, -- 작성일
    REVIEW_UPDATE_DATE DATE, -- 수정일
    REVIEW_DEL_FL CHAR(1) DEFAULT 'N' CHECK (REVIEW_DEL_FL IN ('Y', 'N')), -- 삭제 여부
    REVIEW_TYPE NUMBER NOT NULL, -- 리뷰 유형(1:모집 2:브랜드)
    REVIEW_TYPE_NO NUMBER NOT NULL, -- 유형 번호 (게시글 또는 브랜드 번호)
    MEMBER_NO NUMBER NOT NULL, -- 작성자 회원 번호
    ORDER_NO NUMBER NULL, -- 주문 고유 번호 (한 주문에 한 리뷰만 가능)
    CONSTRAINT FK_REVIEW_MEMBER FOREIGN KEY (MEMBER_NO) REFERENCES MEMBER(MEMBER_NO),
    CONSTRAINT FK_REVIEW_ORDER FOREIGN KEY (ORDER_NO) REFERENCES "ORDER"(ORDER_NO),
    CONSTRAINT UQ_REVIEW_ORDER UNIQUE (ORDER_NO) -- 한 주문에 하나의 리뷰만 가능
);

-- 포인트 사용 내역 테이블
CREATE TABLE POINT_USAGE (
    USAGE_NO NUMBER PRIMARY KEY, -- 포인트 사용 내역 고유 번호
    USED_AMOUNT NUMBER NOT NULL, -- 사용한 포인트 금액
    USAGE_TYPE NUMBER NOT NULL, -- 사용 유형 (1:상품 구매,2:공동구매)
    USAGE_TYPE_NO NUMBER NOT NULL, -- 유형 번호(식별 번호)
    STATUS VARCHAR2(20) DEFAULT '대기' NOT NULL, -- 사용 상태 ('대기', '완료', '취소' )
    USED_DATE DATE DEFAULT SYSDATE NOT NULL, -- 사용 날짜
    MEMBER_NO NUMBER NOT NULL, -- 회원 번호
    FOREIGN KEY (MEMBER_NO) REFERENCES MEMBER(MEMBER_NO)
);

-- 포인트 충전 내역 테이블
CREATE TABLE POINT_TRANSACTION (
    TRANSACTION_NO NUMBER PRIMARY KEY, -- 포인트 충전 내역 고유 번호
    AMOUNT NUMBER NOT NULL, -- 충전한 포인트 금액
    PAYMENT_METHOD VARCHAR2(50) NOT NULL, -- 결제 방식 (간편결제, 계좌이체 등)
    CHARGED_DATE DATE DEFAULT SYSDATE NOT NULL, -- 충전 날짜
    MEMBER_NO NUMBER NOT NULL, -- 회원 번호
    FOREIGN KEY (MEMBER_NO) REFERENCES MEMBER(MEMBER_NO)
);

-- 댓글 테이블
CREATE TABLE REPLY (
    REPLY_NO NUMBER PRIMARY KEY, -- 댓글 고유 번호
    REPLY_CREATE_DATE DATE DEFAULT SYSDATE NOT NULL, -- 작성일
    REPLY_DEL_FL CHAR(1) DEFAULT 'N' CHECK (REPLY_DEL_FL IN ('Y', 'N')), -- 댓글 삭제 여부(Y: 삭제O, N: 삭제 X)
    REPLY_CONTENT VARCHAR2(1000) NOT NULL, -- 댓글 내용
    SECRET_REPLY_STATUS CHAR(1) DEFAULT 'N' CHECK (SECRET_REPLY_STATUS IN ('Y', 'N')), -- 비밀글 여부
    MEMBER_NO NUMBER NOT NULL, -- 작성자 회원 번호
    REPLY_TYPE NUMBER NOT NULL, -- 댓글 유형( 1:게시판, 2:리뷰)
    REPLY_TYPE_NO NUMBER NOT NULL, -- 댓글 유형 코드(식별 코드)
    PARENT_REPLY_NO NUMBER, -- 부모 댓글 번호
    FOREIGN KEY (MEMBER_NO) REFERENCES MEMBER(MEMBER_NO)
);


-- 장바구니 테이블
CREATE TABLE CART (
    BOARD_NO NUMBER NOT NULL, -- 게시글 번호
    MEMBER_NO NUMBER NOT NULL, -- 회원 번호
    ADDED_DATE DATE DEFAULT SYSDATE NOT NULL, -- 장바구니에 담은 시간
    PRIMARY KEY (BOARD_NO, MEMBER_NO),
    FOREIGN KEY (BOARD_NO) REFERENCES BOARD(BOARD_NO),
    FOREIGN KEY (MEMBER_NO) REFERENCES MEMBER(MEMBER_NO)
);


-- 찜 테이블
CREATE TABLE PICK (
    MEMBER_NO NUMBER NOT NULL, -- 회원 번호
    BOARD_NO NUMBER NOT NULL, -- 게시글 번호
    PRIMARY KEY (MEMBER_NO, BOARD_NO),
    FOREIGN KEY (MEMBER_NO) REFERENCES MEMBER(MEMBER_NO),
    FOREIGN KEY (BOARD_NO) REFERENCES BOARD(BOARD_NO)
