-- 공동구매 모집방 테이블
CREATE TABLE RECRUITMENT_ROOM (
    RECRUITMENT_NO NUMBER PRIMARY KEY, -- 모집방 고유 번호
    MAX_PARTICIPANTS VARCHAR2(500) NOT NULL, -- 모집 최대 인원
    RECRUITMENT_STATUS VARCHAR2(500) DEFAULT '진행' NOT NULL, -- 모집 상태 (진행, 마감, 완료, 정지)
    REC_CREATED_DATE DATE DEFAULT SYSDATE NOT NULL, -- 모집 생성일
    REC_END_DATE DATE NOT NULL, -- 모집 마감일
    REGION VARCHAR2(1000) NOT NULL, -- 지역
    PRODUCT_URL VARCHAR2(3000) NOT NULL, -- 상품 URL
    QR_CODE NUMBER NOT NULL, -- QR 코드
    MEMBER_NO NUMBER NOT NULL, -- 회원 번호 (방장)
    BOARD_NO NUMBER NOT NULL, -- 게시글 번호
    FOREIGN KEY (MEMBER_NO) REFERENCES MEMBER(MEMBER_NO),
    FOREIGN KEY (BOARD_NO) REFERENCES BOARD(BOARD_NO)
);

-- 공동구매 참가자 테이블
CREATE TABLE RECRUITMENT_PARTICIPANT (
    MEMBER_NO NUMBER NOT NULL, -- 회원 번호
    JOIN_DATE DATE DEFAULT SYSDATE NOT NULL, -- 참가일
    CANCEL_DATE DATE, -- 취소일
    RECRUITMENT_NO NUMBER NOT NULL, -- 모집방 번호
    PARTICIPATION_COUNT NUMBER NOT NULL, -- 내 몫
    PRIMARY KEY (MEMBER_NO, RECRUITMENT_NO),
    FOREIGN KEY (MEMBER_NO) REFERENCES MEMBER(MEMBER_NO),
    FOREIGN KEY (RECRUITMENT_NO) REFERENCES RECRUITMENT_ROOM(RECRUITMENT_NO)
);



-- 이미지 테이블
CREATE TABLE IMG (
    IMG_NO NUMBER PRIMARY KEY, -- 이미지 고유 번호
    IMG_RENAME VARCHAR2(500) NOT NULL, -- 변경명
    IMG_PATH VARCHAR2(500) NOT NULL, -- 이미지 저장 경로
    IMG_ORIGINAL VARCHAR2(500) NOT NULL, -- 원본명
    IMG_LEVEL NUMBER NOT NULL, -- 이미지 위치 지정 번호
    IMG_TYPE NUMBER NOT NULL, -- 게시판 유형 (1: 브랜드 상품, 2: 개인 모공, 3: 브랜드 리뷰 등)
    IMG_TYPE_NO NUMBER NOT NULL -- 유형 번호 (특정 게시물과 연계)
);

-- 채팅방 테이블
CREATE TABLE CHAT_ROOM (
    ROOM_NO NUMBER PRIMARY KEY, -- 채팅방 고유 번호
    ROOM_NAME VARCHAR2(255) NOT NULL, -- 채팅방 이름
    GROUP_FL CHAR(1) DEFAULT 'N' CHECK (GROUP_FL IN ('Y', 'N')), -- 그룹 채팅 여부 (N:1:1, Y: 그룹 채팅)
    CREATED_DATE DATE DEFAULT SYSDATE NOT NULL, -- 생성 날짜
    ROOM_DELETED_FL CHAR(1) DEFAULT 'N' CHECK (ROOM_DELETED_FL IN ('Y', 'N')), -- 채팅방 삭제 여부 (Y: 삭제 O, N: 삭제 X)
    OWNER_MEMBER_NO NUMBER NOT NULL, -- 채팅방 만든 사람
    FOREIGN KEY (OWNER_MEMBER_NO) REFERENCES MEMBER(MEMBER_NO)
);

-- 채팅 메시지 테이블
CREATE TABLE CHAT_MESSAGE (
    MESSAGE_NO NUMBER PRIMARY KEY, -- 메시지 고유 번호
    MESSAGE_CONTENT VARCHAR2(1000) NOT NULL, -- 메시지 내용
    MESSAGE_TYPE VARCHAR2(10) NOT NULL, -- 메시지 타입 (TEXT, IMAGE, EMOJI 등)
    SEND_DATE DATE DEFAULT SYSDATE NOT NULL, -- 보낸 시간
    MESSAGE_DEL_FL CHAR(1) DEFAULT 'N' CHECK (MESSAGE_DEL_FL IN ('Y', 'N')), -- 삭제 여부 ('N': 정상, 'Y': 삭제됨)
    MEMBER_NO NUMBER NOT NULL, -- 회원 번호
    ROOM_NO NUMBER NOT NULL, -- 채팅방 번호
    FOREIGN KEY (MEMBER_NO) REFERENCES MEMBER(MEMBER_NO),
    FOREIGN KEY (ROOM_NO) REFERENCES CHAT_ROOM(ROOM_NO)
);

-- 채팅 알림 테이블
CREATE TABLE CHAT_NOTIFICATION (
    NOTI_ID NUMBER PRIMARY KEY, -- 알림 ID
    UNREAD_COUNT NUMBER NOT NULL, -- 읽지 않은 메시지 개수
    MEMBER_NO NUMBER NOT NULL, -- 회원 번호
    MESSAGE_NO NUMBER NOT NULL, -- 마지막으로 읽지 않은 메시지 번호
    ROOM_NO NUMBER NOT NULL, -- 채팅방 번호
    FOREIGN KEY (MEMBER_NO) REFERENCES MEMBER(MEMBER_NO),
    FOREIGN KEY (MESSAGE_NO) REFERENCES CHAT_MESSAGE(MESSAGE_NO),
    FOREIGN KEY (ROOM_NO) REFERENCES CHAT_ROOM(ROOM_NO)
);



-- 채팅방-사용자 테이블
CREATE TABLE CHAT_ROOM_USER (
    JOINED_DATE DATE DEFAULT SYSDATE NOT NULL, -- 가입한 시간
    MEMBER_NO NUMBER NOT NULL, -- 회원 번호
    ROOM_NO NUMBER NOT NULL, -- 채팅방 번호
    PRIMARY KEY (MEMBER_NO, ROOM_NO),
    FOREIGN KEY (MEMBER_NO) REFERENCES MEMBER(MEMBER_NO),
    FOREIGN KEY (ROOM_NO) REFERENCES CHAT_ROOM(ROOM_NO)
);

-- 이모티콘 테이블
CREATE TABLE CHAT_EMOJI (
    EMOJI_NO NUMBER PRIMARY KEY, -- 이모티콘 고유 번호
    MESSAGE_ID NUMBER NOT NULL, -- 메시지 ID
    EMOJI_CODE VARCHAR2(50) NOT NULL, -- 이모티콘 코드
    FOREIGN KEY (MESSAGE_ID) REFERENCES CHAT_MESSAGE(MESSAGE_NO)
);

-- 채팅 FAQ 테이블
CREATE TABLE CHAT_FAQ (
    FAQ_NO NUMBER PRIMARY KEY, -- 질문 번호
    FAQ_MENU VARCHAR2(500) NOT NULL, -- 질문 내용
    FAQ_MENU_ANSWER VARCHAR2(2000) NOT NULL, -- 질문 답변
    ROOM_NO NUMBER NOT NULL, -- 채팅방 번호
    FOREIGN KEY (ROOM_NO) REFERENCES CHAT_ROOM(ROOM_NO)
);

-- 신고 테이블
CREATE TABLE REPORT (
    REPORT_NO NUMBER NOT NULL,  -- 신고 번호
    REPORT_DETAIL VARCHAR2(3000) NOT NULL,  -- 신고 내용
    REPORT_TITLE VARCHAR2(1000) NOT NULL,  -- 신고 제목
    REPORT_DATE DATE NOT NULL,  -- 신고 일자
    REPORT_STATUS VARCHAR2(30) DEFAULT '대기' NOT NULL,  -- 신고 처리 상태('대기', '처리 완료', '반려', '블랙', '경고')
    REPORT_TYPE NUMBER NOT NULL,  -- 신고 유형
    REPORT_TYPE_NO NUMBER NOT NULL,  -- 신고 유형(1: 브랜드 상품, 2: 개인 상품, 3: 댓글, 4: 채팅, 5: 리뷰)
    REPORT_M_REPLY VARCHAR2(3000) ,  -- 신고 답글
    MEMBER_NO NUMBER NOT NULL,  -- 신고한 회원 번호
    REPORTED_USER_NO NUMBER NOT NULL,  -- 신고 대상 회원 번호
    CONSTRAINT PK_REPORT PRIMARY KEY (REPORT_NO),  -- 신고 번호 기본키
    CONSTRAINT FK_MEMBER_TO_REPORT FOREIGN KEY (MEMBER_NO) REFERENCES MEMBER(MEMBER_NO),  -- 신고한 회원 외래키
    CONSTRAINT FK_REPORTED_USER FOREIGN KEY (REPORTED_USER_NO) REFERENCES MEMBER(MEMBER_NO)  -- 신고 대상 회원 외래키
);

